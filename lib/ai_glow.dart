/// A Flutter package that provides beautiful glowing effects for your widgets.
///
/// This package offers two main widgets:
/// * [InnerAiGlowing] - Creates a glowing effect inside the widget
/// * [OuterAiGlow] - Creates a glowing effect around the widget
///
/// Both widgets support customizable colors, glow width, blur effect, and border radius.
library ai_glow;

export 'src/inner_ai_glow.dart';
export 'src/outer_ai_glow.dart';

/// A Calculator.
class Calculator {
  /// Returns [value] plus 1.
  int addOne(int value) => value + 1;
}
